namespace RSG.Core.Constants
{
    /// <summary>
    /// Represents display name constants and their character sets.
    /// </summary>
    public static class CharacterSetConstants
    {
        /// <summary>
        /// Display name constant Lowercase.
        /// </summary>
        public const string Lowercase = nameof(Lowercase);

        /// <summary>
        /// Lowercase letter set.
        /// </summary>
        public const string LowercaseSet = "abcdefghijklmnopqrstuvwxyz";

        /// <summary>
        /// Display name constant Uppercase.
        /// </summary>
        public const string Uppercase = nameof(Uppercase);

        /// <summary>
        /// Uppercase letter set.
        /// </summary>
        public const string UppercaseSet = "ABCDEFGHIJKLMNAOPQRSTUVWXYZ";

        /// <summary>
        /// Display name constant Numbers.
        /// </summary>
        public const string Numbers = nameof(Numbers);

        /// <summary>
        /// Numbers set.
        /// </summary>
        public const string NumbersSet = "0123456789";

        /// <summary>
        /// Display name constant Punctuation.
        /// </summary>
        public const string Punctuation = nameof(Punctuation);

        /// <summary>
        /// Punctuation set.
        /// </summary>
        public const string PunctuationSet = "!,.?";

        /// <summary>
        /// Display name constant Space.
        /// </summary>
        public const string Space = nameof(Space);

        /// <summary>
        /// Literal space.
        /// </summary>
        public const string SpaceSet = " ";

        /// <summary>
        /// Display name constant Symbols.
        /// </summary>
        public const string Symbols = nameof(Symbols);

        /// <summary>
        /// Symbols set.
        /// </summary>
        public const string SymbolsSet = @"!@#$%^&*()-=_+,./;\'\<>?:[]{}|`~";
    }
}
